class Exo_3 {
    public static void main(String[] args) {
        
        byte myByte = 100;
        short myShort = 1000;
        int myInt = 10000;
        long myLong = 100000L;
        float myFloat = 10.5f;
        double myDouble = 20.5;
        char myChar = 'A';
        boolean myBoolean = true;

        
        String myString = "Hello, World!";
        Integer myInteger = 10000;
        Double myDoubleRef = 20.5;
        Character myCharacter = 'A';
        Boolean myBooleanRef = true;

        
        
        System.out.println("byte: " + myByte);
        System.out.println("short: " + myShort);
        System.out.println("int: " + myInt);
        System.out.println("long: " + myLong);
        System.out.println("float: " + myFloat);
        System.out.println("double: " + myDouble);
        System.out.println("char: " + myChar);
        System.out.println("boolean: " + myBoolean);

       
        System.out.println("String: " + myString);
        System.out.println("Integer: " + myInteger);
        System.out.println("Double: " + myDoubleRef);
        System.out.println("Character: " + myCharacter);
        System.out.println("Boolean: " + myBooleanRef);
    }
}